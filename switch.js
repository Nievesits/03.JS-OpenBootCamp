//Switch

let nota = 9;

switch (nota){
    case 5:
        console.log ("buen trabajo");
        break;
    case 4:
        console.log ("Notable");
        break;
    case 3:
        console.log ("Suficiente");
        break;
    case 2:
        console.log ("Insuficiente");
        break;
    case 1:
        console.log ("Suspenso");
        break;
    default: 
    console.log ("Error");
    break;
}